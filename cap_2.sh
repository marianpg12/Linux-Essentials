Funciones del SO Moderno: Además de gestionar recursos, los SO modernos permiten la ejecución de múltiples tareas a la vez (multi-tasking), manejan solicitudes del usuario y proveen servicios como impresiones o ejecución de programas.

Tipos de SO: Los SO pueden ser simples, como los que corren en dispositivos de un solo propósito (firewalls, teléfonos), o complejos como los que gestionan sistemas de escritorio o servidores.

Interacción con el Usuario: Los sistemas modernos permiten interactuar mediante interfaces gráficas (GUI), aunque para tareas de administración en sistemas Unix o Linux, es común el uso de la terminal (CLI).

Sistemas Operativos Populares: Los tres principales son:

Microsoft Windows: Código propietario, único en su base.
Apple macOS: Basado en UNIX (BSD).
Linux: Basado en UNIX, disponible en múltiples distribuciones.
Rol del Sistema: Al definir una máquina, se decide su función: ¿Será un servidor o un sistema de escritorio? Los servidores suelen usar CLI para ahorrar recursos.

Ciclo de Vida del SO: Los sistemas y software tienen un ciclo de vida que incluye actualizaciones y soporte. En entornos empresariales, los ciclos de mantenimiento y actualización son cruciales.

Virtualización y Cloud: La virtualización permite a un solo equipo físico ejecutar varias máquinas virtuales, optimizando recursos. Los servicios en la nube han disminuido la necesidad de actualizaciones de hardware.

Estabilidad de Software: Las versiones de software pueden ser beta (nuevas funciones, menos pruebas) o estables (más testeadas). Las versiones beta son más arriesgadas para entornos de producción.

Compatibilidad hacia Atrás: Asegura que las nuevas versiones de un SO sean compatibles con software anterior, algo común en el desarrollo de software de código abierto.

Costo: Consideraciones de costo incluyen licencias (como en Microsoft) y gastos de mantenimiento. La virtualización y los servicios en la nube ayudan a controlar los costos.

Interfaces del SO: Los primeros sistemas usaban terminales de texto (CLI), mientras que hoy en día la mayoría de los SO incluyen tanto GUI como CLI.
"
